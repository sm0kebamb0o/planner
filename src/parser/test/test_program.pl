* Тестовая программа на языке Плэннер
* Комментарии пишутся как атомы — они вычисляются в себя и игнорируются

* --- Простые атомы ---
ABC
42
-9
2.71828
*3704

* --- Арифметика ---
[+ 4 -3 0]
[× 3 7]
[- [+ 3 6] [- 4 2]]

* --- Списки (L-списки) ---
(A B C)
()
(1 2.0 ABC)

* --- Вложенные списки ---
((1 2) (3 4) (5 6))

* --- Операции над списками ---
[LENGTH (A B C D)]
[ELEM 2 (A B C D)]
[REST 1 (A B C D)]
[HEAD 2 (A B C D)]

* --- Предикаты ---
[NUM 42]
[ATOM ABC]
[LIST (1 2 3)]
[EMPTY ()]
[EQ (A B) (QUOTE (A B))]

* --- Переменная и константа через PROG и CSET ---
[CSET PI 3.14159]
:PI
[PROG (X)
  [SET X 10]
  [+ .X 5]]

* --- Условное выражение ---
[COND (() первая-клауза-ложна)
      (T  нашли-истинную-клаузу)]

* --- Определение и вызов функции ---
[DEFINE SQUARE (LAMBDA (N) [× .N .N])]
[SQUARE 7]

* --- Рекурсивная функция: факториал ---
[DEFINE FACT (LAMBDA (N)
  [COND ([LE .N 1] 1)
        (T [× .N [FACT [- .N 1]]])])]
[FACT 5]

* --- Цикл FOR: сумма 1 до 5 ---
[PROG (S)
  [SET S 0]
  [FOR I 5 [SET S [+ .S .I]]]
  .S]

* --- Сегментный вызов ---
<REST 1 (A B C D)>
