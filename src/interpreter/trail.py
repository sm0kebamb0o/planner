"""Trail (стек отката) для режима возвратов Плэнера.

Trail записывает обратные операторы — функции, отменяющие побочные эффекты
мутирующих операций (SET, ADD1, SUB1, FIN, CSET). При откате по неуспеху
обратные операторы выполняются в обратном порядке, восстанавливая состояние
программы на момент создания последней развилки.
"""
from __future__ import annotations

from typing import Callable


# Сентинель: переменная существует, но не имеет значения (UNASSIGN).
# Используется в обратных операторах для различения «вернуть старое значение»
# и «удалить значение (UNASSIGN)».
_UNBOUND = object()


class Trail:
    """Стек обратных операторов.

    Основные операции:
      mark()        — вернуть текущую позицию в стеке (целое число)
      push_undo(fn) — добавить обратный оператор
      undo_to(m)    — выполнить все операторы с позиции m до конца,
                      восстанавливая состояние (режим возвратов)
      discard_to(m) — удалить операторы с позиции m без выполнения
                      (используется в PERM: побочные эффекты «зафиксированы»)
    """

    __slots__ = ("_ops",)

    def __init__(self) -> None:
        self._ops: list[Callable[[], None]] = []

    # ------------------------------------------------------------------

    def mark(self) -> int:
        """Вернуть текущую позицию (число записанных операторов)."""
        return len(self._ops)

    def push_undo(self, fn: Callable[[], None]) -> None:
        """Записать обратный оператор в стек."""
        self._ops.append(fn)

    def undo_to(self, mark: int) -> None:
        """Откатить состояние: выполнить операторы с конца до позиции mark."""
        while len(self._ops) > mark:
            self._ops.pop()()

    def discard_to(self, mark: int) -> None:
        """Отбросить операторы начиная с позиции mark без выполнения.

        Используется в PERM: все изменения, сделанные внутри выражения,
        считаются «постоянными» — обратные операторы больше не нужны.
        """
        del self._ops[mark:]
